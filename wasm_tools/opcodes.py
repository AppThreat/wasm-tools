"""
Opcode table mapping (prefix, opcode) -> (mnemonic, ImmType).

Covers:
  - Core MVP opcodes (0x00-0xC4)
  - Bulk-memory / saturating-truncation (0xFC 0-11)
  - Table bulk ops (0xFC 12-17)
  - SIMD / vector (0xFD prefix)
  - GC / reference types (0xFB prefix)
  - Threads / atomics (0xFE prefix)
  - Exceptions / control-flow extensions (0x08, 0x0A, 0x1F, 0xD5, 0xD6)
  - Return-call extensions (0x12, 0x13, 0x14, 0x15)
"""


class ImmType:
    NONE = 0
    BLOCK_SIG = 1
    INDEX = 2
    MEMARG = 3
    I32 = 4
    I64 = 5
    F32 = 6
    F64 = 7
    BR_TABLE = 8
    CALL_INDIRECT = 9
    MEMORY_INIT = 10
    MEMORY_COPY = 11
    # Extended types
    SELECT_T = 12  # typed select: vec of val types (skip them)
    HEAP_TYPE = 13  # single heaptype immediate (LEB s33)
    TWO_INDICES = 14  # two u32 LEB indices
    TABLE_INIT = 15  # elem_idx + table_idx (two u32)
    TABLE_COPY = 16  # table_dst + table_src (two u32)
    V128_CONST = 17  # 16 raw bytes
    V128_SHUFFLE = 18  # 16 lane-index bytes
    LANE_IDX = 19  # single byte lane index
    MEMARG_LANE = 20  # memarg (align + offset) + lane byte
    BR_ON_CAST = 21  # castflags byte + label + 2 heap types
    TRY_TABLE_BLOCK = 22  # blocktype + catch list
    ATOMIC_FENCE = 23  # single reserved byte (0x00)


